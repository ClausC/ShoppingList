package com.clausc.shoppinglist.model;

public class Item {
	Integer id;
	String name;
	Integer count;

	public Integer getId() {
  	return id;
  }
	public void setId(Integer id) {
  	this.id = id;
  }
	public String getName() {
  	return name;
  }
	public void setName(String name) {
  	this.name = name;
  }
	public Integer getCount() {
  	return count;
  }
	public void setCount(Integer count ) {
  	this.count = count;
  }

	
	
}
